class Arith
{
public static void main(String args[])
{
int a=4,b=4;
System.out.println("***PROGRAM ON COMPOUND ARITHMETIC OPERATORS***");
a+=b;
System.out.println("the value of a="+a);
a-=b;
System.out.println("the value of a="+a);
a/=b;
System.out.println("the value of a="+a);
a*=b;
System.out.println("the value of a="+a);
a%=b;
System.out.println("the value of a="+a);
}
}
